    private void jButton1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jButton1MouseClicked
        List<Admin> admins=getAdmins();
        Admin admin=admins.get(0);
        if(!campoPassword.getText().equals("")&&!campoUsuario.getText().equals("")){
            boolean inicioValido=false;
            for(Usuario usuario:admin.getUsuarios()){
                if(usuario.getPassword().equals(campoPassword.getText())&&usuario.getUsuario().equals(campoUsuario.getText())){
                    inicioValido=true;
                    if(usuario instanceof Admin){
                        panelPrincipal.removeAll();
                        AdministradorPanel panelAdmin=new AdministradorPanel((Admin)usuario);
                        panelAdmin.setVisible(true);
                        panelPrincipal.add(panelAdmin);
                        panelPrincipal.validate();
                    }else{
                        panelPrincipal.removeAll();
                        UserPanel panelUsuario=new UserPanel(usuario,admin);
                        panelUsuario.setVisible(true);
                        panelPrincipal.add(panelUsuario);
                        panelPrincipal.validate();
                    }
                }
            }
            if(!inicioValido){
                JOptionPane.showMessageDialog(null,"Usuario y/o contraseña invalidos");
            }
        }else{
            JOptionPane.showMessageDialog(null,"Ingrese usuario y contraseña");
        }
    }//GEN-LAST:event_jButton1MouseClicked

