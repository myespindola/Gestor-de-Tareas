    public static List<Admin> getAdmins(){
        File admin=new File("usuarios.txt");
        List<Admin> admins = new ArrayList<Admin>();
        try (ObjectInputStream in = new ObjectInputStream(new BufferedInputStream(new FileInputStream(admin)))) {
            while(true) {
                Object object = in.readObject();
                if(object instanceof Admin)
                    admins.add((Admin)object);
           }
       }catch (IOException e) {
           
       }catch(ClassNotFoundException ex){
           
       }finally{
            if(admins.isEmpty()){
                admins.add(new Admin("1","Admin","Administrador","Admin","admin"));
            }
            return admins;
       }
    }

